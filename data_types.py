# Demonstrating Fundamental Data Types in Python

# Sequential Data Types

# List
my_list = [1, 2, 3, 4, 5]
print(f"my_list: {my_list}, type: {type(my_list)}")

# Tuple
my_tuple = (1, 2, 3, 4, 5)
print(f"my_tuple: {my_tuple}, type: {type(my_tuple)}")

# String
my_string = "Hello, Python!"
print(f"my_string: {my_string}, type: {type(my_string)}")

# Non-Sequential Data Types

# Set
my_set = {1, 2, 3, 4, 5}
print(f"my_set: {my_set}, type: {type(my_set)}")

# Dictionary
my_dict = {'a': 1, 'b': 2, 'c': 3}
print(f"my_dict: {my_dict}, type: {type(my_dict)}")

# Additional Fundamental Data Types

# Integer
my_int = 10
print(f"my_int: {my_int}, type: {type(my_int)}")

# Float
my_float = 10.5
print(f"my_float: {my_float}, type: {type(my_float)}")

# Boolean
my_bool = True
print(f"my_bool: {my_bool}, type: {type(my_bool)}")

